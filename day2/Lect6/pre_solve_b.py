"""
Легкий способ многопараметрической сортировки
в задаче B
"""

WORDS = ["три","четыре", "пять", "шесть", "два", "один"]
        #["два" , "три", "один", "пять", "шесть", "четыре"]


def main():
    """
    Входная точка
    """
    print(sorted(WORDS, key=lambda elem: (len(elem), elem)))

if __name__ == "__main__":
    main()
